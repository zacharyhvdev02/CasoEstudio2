    private void buttonActionPerformed(java.awt.event.ActionEvent evt) {// GEN-FIRST:event_buttonActionPerformed
        String username = usernameTxt.getText();
        char[] password = passwordTxt.getPassword();

        if (username.isEmpty() || password.length == 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos. \n", "AVISO",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            usernameTxt.requestFocus();
            return;
        }

        try {
            DAOUsers daoUsers = new DAOUsersImpl();

            int userId = daoUsers.login(username, new String(password));
            if (userId == -1) {
                javax.swing.JOptionPane.showMessageDialog(this, "Usuario o contrase침a incorrectos. \n",
                        "AVISO", javax.swing.JOptionPane.ERROR_MESSAGE);
                usernameTxt.requestFocus();
                return;
            }

            com.mycompany.models.Users currentUser = daoUsers.getUserById(userId);

            javax.swing.JOptionPane.showMessageDialog(this,
                    "춰Bienvenido " + currentUser.getName() + " " + currentUser.getLast_name_p()
                            + " " + currentUser.getLast_name_m()
                            + "!\n",
                    "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);

            ClientSession.getInstance().setUserId(userId);
            ClientDashboard.ShowJPanel(new ClientBooks(false));
        } catch (Exception e) {
            javax.swing.JOptionPane.showMessageDialog(this, "Ocurri칩 un error al iniciar sesi칩n. \n",
                    "AVISO",
                    javax.swing.JOptionPane.ERROR_MESSAGE);
            System.out.println(e.getMessage());
        }
    }// GEN-LAST:event_buttonActionPerformed

